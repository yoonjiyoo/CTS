SELECT DISTINCT(A.CAR_ID), A.CAR_TYPE, A.DAILY_FEE*(1-(C.DISCOUNT_RATE/100))*30 AS FEE
FROM CAR_RENTAL_COMPANY_CAR A 
JOIN CAR_RENTAL_COMPANY_RENTAL_HISTORY B ON A.CAR_ID=B.CAR_ID
JOIN CAR_RENTAL_COMPANY_DISCOUNT_PLAN C ON A.CAR_TYPE=C.CAR_TYPE
WHERE A.CAR_ID NOT IN (
SELECT CAR_ID FROM CAR_RENTAL_COMPANY_RENTAL_HISTORY
WHERE TO_CHAR(END_DATE,'YYYY-MM-DD') BETWEEN '2022-11-01' AND '2022-11-30'
OR TO_CHAR(START_DATE,'YYYY-MM-DD') BETWEEN '2022-11-01' AND '2022-11-30'
OR TO_CHAR(END_DATE,'YYYY-MM-DD')>'2022-11-01')
AND C.DURATION_TYPE='30일 이상'
AND A.CAR_TYPE IN ('세단','SUV')
AND A.DAILY_FEE*(1-(C.DISCOUNT_RATE/100))*30 BETWEEN 500000 AND 1999999
ORDER BY FEE DESC, CAR_TYPE ASC, CAR_ID DESC
